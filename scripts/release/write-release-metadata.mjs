#!/usr/bin/env node
// Overwrites src/lib/config/release-metadata.generated.ts with this run's real values, BEFORE
// `npm run build` runs in CI. See that file's header comment for why this is a build-time-baked
// constant rather than a runtime env var. Never run this against a working tree you care about
// keeping clean locally — it's meant for CI's ephemeral checkout only.
//
// Usage:
//   node scripts/release/write-release-metadata.mjs --sha <git-sha> --run-id <github-run-id>
import { writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { parseArgs, isPlausibleGitSha } from './lib.mjs';

const TARGET = join(process.cwd(), 'src', 'lib', 'config', 'release-metadata.generated.ts');

function main() {
  const args = parseArgs(process.argv.slice(2));
  const sha = args.sha;
  const runId = args['run-id'];
  if (!isPlausibleGitSha(sha)) throw new Error(`--sha is not a plausible git SHA: ${sha}`);
  if (!runId) throw new Error('--run-id is required');
  const timestamp = new Date().toISOString();

  const content = `/**
 * GENERATED by scripts/release/write-release-metadata.mjs for GitHub Actions run ${runId}.
 * Do not edit or commit this — it's regenerated every CI run and reset to the committed null
 * defaults in every real commit (see git history / release-metadata.generated.ts on \`main\`).
 */
export const RELEASE_SHA: string | null = ${JSON.stringify(sha)};
export const RELEASE_RUN_ID: string | null = ${JSON.stringify(String(runId))};
export const RELEASE_BUILD_TIMESTAMP: string | null = ${JSON.stringify(timestamp)};
`;
  writeFileSync(TARGET, content);
  console.log(`✓ wrote ${TARGET} (sha=${sha}, runId=${runId})`);
}

main();
